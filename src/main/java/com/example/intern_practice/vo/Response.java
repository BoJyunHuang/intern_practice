package com.example.intern_practice.vo;

public class Response {

	private String message;

	public Response() {
		super();
	}
	
	public Response(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
}
